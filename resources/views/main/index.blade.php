    window.onload = function () {
        let allDataElement = document.getElementById('allDataChart').getContext('2d');
        window.allLine     = new Chart(allDataElement, allDataConfig);

        $.get(`/api/nav?start=&end=`, data => {
            let reversedData = data.reverse();
            reversedData.shift(); // temporary
            let filteredData = reversedData.filter((item, index) => {
                let month     = +item.created_at.slice(3, 5);
                let nextMonth = reversedData[index + 1] && +reversedData[index + 1].created_at.slice(3, 5);

                return month !== nextMonth || index === 0
            });

            let averageData = filteredData.map((item, index) => {
                return filteredData[index + 1] && {
                    value: (filteredData[index + 1].value / item.value - 1).toFixed(4),
                    created_at: filteredData[index + 1].created_at
                }
            });

            averageData.pop();

            allDataConfig.data.labels = reversedData.map(item => {
                return item.created_at;
            });

            allDataConfig.data.datasets.forEach(function (dataset) {
                dataset.data = data.map(x => x.value);
            });

            window.allLine.update();
        });
    };

