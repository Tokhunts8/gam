        $('.all-data-graphic, .average-data-graphic').on('click', function (e) {

            let date  = new Date();
            let day   = date.getDay().toString().padStart(2, 0);
            let month = (date.getMonth() + 1).toString().padStart(2, 0);
            let year  = date.getFullYear();

            let dateNow = `${day}-${month}-${year}`;

            let isAllData = $(this).hasClass('all-data-graphic');

            isAllData ? $('.all-data-graphic').removeClass('active') : $('.average-data-graphic').removeClass('active');
            $(this).addClass('active');

            switch (e.currentTarget.dataset.id) {
                case 'threeMonths' : {
                    date.setMonth(date.getMonth() - 3);
                    getData(`${date.getDay().toString().padStart(2, 0)}-${(date.getMonth() + 1).toString().padStart(2, 0)}-${date.getFullYear()}`, dateNow, isAllData)
                }
                    break;
                case 'sixMonths' : {
                    date.setMonth(date.getMonth() - 6);
                    getData(`${date.getDay().toString().padStart(2, 0)}-${(date.getMonth() + 1).toString().padStart(2, 0)}-${date.getFullYear()}`, dateNow, isAllData)
                }
                    break;
                case 'thisYear' : {
                    getData(`01-01-${year}`, dateNow, isAllData)
                }
                    break;
                case 'oneYear' : {
                    getData(`${day}-${month}-${year - 1}`, dateNow, isAllData)
                }
                    break;
                // case 'threeYear' : {
                //     getData(`${day}-${month}-${year - 3}`, dateNow)
                // }
                //     break;
                case 'allTime' : {
                    getData('', '', isAllData)
                }
                    break;

                default:
                    break
            }

        });

